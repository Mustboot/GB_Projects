#! /usr/bin/python3

# ====== Задание 1 ======
# Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

# ====== Решение: =======
print(f'Результат: {15 * 3} / Тип результата: {type(15 * 3)}')
print(f'Результат: {15 / 3} / Тип результата: {type(15 / 3)}')
print(f'Результат: {15 // 2} / Тип результата: {type(15 // 2)}')
print(f'Результат: {15 ** 2} / Тип результата: {type(15 ** 2)}')
